@main.route("/upload/<int:job_id>", methods=["GET", "POST"])
@login_required
def upload(job_id):

    job = Job.query.get_or_404(job_id)

    if request.method == "POST":

        file = request.files["resume"]
        filename = secure_filename(file.filename)

        upload_folder = "uploads"

        if not os.path.exists(upload_folder):
            os.makedirs(upload_folder)

        filepath = os.path.join(upload_folder, filename)
        file.save(filepath)

        resume_text = ""

        # TXT
        if filename.endswith(".txt"):
            with open(filepath, "r", errors="ignore") as f:
                resume_text = f.read().lower()

        # PDF
        elif filename.endswith(".pdf"):
            reader = PdfReader(filepath)

            for page in reader.pages:
                text = page.extract_text()
                if text:
                    resume_text += text.lower()

        resume_skills = extract_skills(resume_text)

        job_title = job.title.lower()

        job_skills = []

        for role in SKILLS_DB:
            if role in job_title:
                job_skills = SKILLS_DB[role]

        if len(job_skills) == 0:
            job_skills = ["python", "flask", "html", "css", "sql"]

        matched_skills = []
        missing_skills = []

        for skill in job_skills:
            if skill in resume_skills:
                matched_skills.append(skill)
            else:
                missing_skills.append(skill)

        score = int((len(matched_skills) / len(job_skills)) * 100)

        all_jobs = Job.query.all()

        job_scores = []

        for j in all_jobs:

            job_text = (j.title + " " + j.description).lower()

            skills = extract_skills(job_text)

            if len(skills) == 0:
                continue

            matches = 0

            for s in skills:
                if s in resume_skills:
                    matches += 1

            temp_score = int((matches / len(skills)) * 100)

            job_scores.append({
                "job": j,
                "score": temp_score
            })

        job_scores = sorted(job_scores, key=lambda x: x["score"], reverse=True)

        top_jobs = job_scores[:3]

        resume_strength = "Beginner"

        if score >= 80:
            resume_strength = "Strong"
        elif score >= 50:
            resume_strength = "Average"
        else:
            resume_strength = "Weak"

        return render_template(
            "result.html",
            job=job,
            score=score,
            matched_skills=matched_skills,
            missing_skills=missing_skills,
            top_jobs=top_jobs,
            resume_strength=resume_strength
        )

    return render_template("upload_resume.html", job=job)



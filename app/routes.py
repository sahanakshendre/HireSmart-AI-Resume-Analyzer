# DOWNLOAD PDF REPORT
# -----------------------

@main.route("/download-report/<job>/<score>")
def download_report(job, score):

    buffer = io.BytesIO()

    styles = getSampleStyleSheet()

    elements = []

    matched_skills = request.args.getlist("matched")
    missing_skills = request.args.getlist("missing")

    elements.append(Paragraph("HireSmart Resume Analysis Report", styles['Title']))
    elements.append(Spacer(1,20))

    elements.append(Paragraph(f"<b>Recommended Job:</b> {job}", styles['Normal']))
    elements.append(Paragraph(f"<b>Match Score:</b> {score}%", styles['Normal']))

    elements.append(Spacer(1,20))

    elements.append(Paragraph("<b>Matched Skills</b>", styles['Heading3']))
    elements.append(ListFlowable([Paragraph(skill, styles['Normal']) for skill in matched_skills]))

    elements.append(Spacer(1,20))

    elements.append(Paragraph("<b>Missing Skills</b>", styles['Heading3']))
    elements.append(ListFlowable([Paragraph(skill, styles['Normal']) for skill in missing_skills]))

    elements.append(Spacer(1,30))

    elements.append(Paragraph("Generated by HireSmart AI Resume Analyzer", styles['Normal']))

    doc = SimpleDocTemplate(buffer, pagesize=letter)
    doc.build(elements)

    buffer.seek(0)

    return send_file(
        buffer,
        as_attachment=True,
        download_name="resume_report.pdf",
        mimetype="application/pdf"
    )

# -----------------------
# EXTRACT SKILLS FROM TEXT
# -----------------------
def extract_skills(text):

    text = text.lower()
    found_skills = []

    all_skills = []

    for role in SKILLS_DB:
        for skill in SKILLS_DB[role]:
            all_skills.append(skill)

    all_skills = list(set(all_skills))

    for skill in all_skills:
        if skill in text:
            found_skills.append(skill)

    return list(set(found_skills))


